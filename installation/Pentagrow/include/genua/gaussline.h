template <class Functor, class ResultType>
inline void gint_line2(const Functor & f, ResultType & sum, Real a=-1, Real b=1)
{
  // gauss line integration for 2 points
  Real t(b-a);
  sum += 0.500000000000000*t* f(a + 0.211324865405187*t);
  sum += 0.500000000000000*t* f(a + 0.788675134594813*t);
}
